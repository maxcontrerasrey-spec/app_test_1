begin;

create extension if not exists pgcrypto;

create table if not exists public.profiles (
  id uuid primary key references auth.users (id) on delete cascade,
  email text not null unique,
  full_name text,
  job_title text,
  employee_code text,
  department text,
  phone text,
  avatar_url text,
  status text not null default 'pending'
    check (status in ('pending', 'active', 'suspended', 'inactive')),
  is_super_admin boolean not null default false,
  must_reset_password boolean not null default false,
  last_seen_at timestamptz,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.app_roles (
  code text primary key,
  name text not null,
  description text,
  is_active boolean not null default true,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint app_roles_code_format check (code = lower(code))
);

create table if not exists public.user_roles (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles (id) on delete cascade,
  role_code text not null references public.app_roles (code) on delete restrict,
  assigned_at timestamptz not null default timezone('utc', now()),
  assigned_by uuid references public.profiles (id) on delete set null,
  unique (user_id, role_code)
);

create table if not exists public.app_modules (
  code text primary key,
  name text not null,
  route text not null unique,
  description text,
  sort_order integer not null default 0,
  is_active boolean not null default true,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint app_modules_code_format check (code = lower(code))
);

create table if not exists public.role_module_access (
  role_code text not null references public.app_roles (code) on delete cascade,
  module_code text not null references public.app_modules (code) on delete cascade,
  can_view boolean not null default true,
  created_at timestamptz not null default timezone('utc', now()),
  primary key (role_code, module_code)
);

create index if not exists idx_profiles_email on public.profiles (email);
create index if not exists idx_profiles_status on public.profiles (status);
create index if not exists idx_user_roles_user_id on public.user_roles (user_id);
create index if not exists idx_user_roles_role_code on public.user_roles (role_code);
create index if not exists idx_app_modules_sort_order on public.app_modules (sort_order);

create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = timezone('utc', now());
  return new;
end;
$$;

drop trigger if exists trg_profiles_set_updated_at on public.profiles;
create trigger trg_profiles_set_updated_at
before update on public.profiles
for each row
execute function public.set_updated_at();

drop trigger if exists trg_app_roles_set_updated_at on public.app_roles;
create trigger trg_app_roles_set_updated_at
before update on public.app_roles
for each row
execute function public.set_updated_at();

drop trigger if exists trg_app_modules_set_updated_at on public.app_modules;
create trigger trg_app_modules_set_updated_at
before update on public.app_modules
for each row
execute function public.set_updated_at();

create or replace function public.handle_new_auth_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (
    id,
    email,
    full_name,
    job_title,
    phone,
    avatar_url,
    status
  )
  values (
    new.id,
    new.email,
    nullif(trim(coalesce(new.raw_user_meta_data ->> 'full_name', new.raw_user_meta_data ->> 'name')), ''),
    nullif(trim(coalesce(new.raw_user_meta_data ->> 'job_title', new.raw_user_meta_data ->> 'role_name')), ''),
    nullif(trim(new.raw_user_meta_data ->> 'phone'), ''),
    nullif(trim(new.raw_user_meta_data ->> 'avatar_url'), ''),
    case
      when coalesce(new.email_confirmed_at, new.confirmed_at) is null then 'pending'
      else 'active'
    end
  )
  on conflict (id) do update
  set
    email = excluded.email,
    full_name = coalesce(excluded.full_name, public.profiles.full_name),
    job_title = coalesce(excluded.job_title, public.profiles.job_title),
    phone = coalesce(excluded.phone, public.profiles.phone),
    avatar_url = coalesce(excluded.avatar_url, public.profiles.avatar_url),
    status = case
      when public.profiles.status = 'inactive' then public.profiles.status
      else excluded.status
    end,
    updated_at = timezone('utc', now());

  return new;
end;
$$;

create or replace function public.sync_auth_user_profile()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  update public.profiles
  set
    email = new.email,
    full_name = coalesce(
      nullif(trim(coalesce(new.raw_user_meta_data ->> 'full_name', new.raw_user_meta_data ->> 'name')), ''),
      public.profiles.full_name
    ),
    job_title = coalesce(
      nullif(trim(coalesce(new.raw_user_meta_data ->> 'job_title', new.raw_user_meta_data ->> 'role_name')), ''),
      public.profiles.job_title
    ),
    phone = coalesce(
      nullif(trim(new.raw_user_meta_data ->> 'phone'), ''),
      public.profiles.phone
    ),
    avatar_url = coalesce(
      nullif(trim(new.raw_user_meta_data ->> 'avatar_url'), ''),
      public.profiles.avatar_url
    ),
    status = case
      when public.profiles.status = 'inactive' then 'inactive'
      when coalesce(new.email_confirmed_at, new.confirmed_at) is null then 'pending'
      else 'active'
    end,
    updated_at = timezone('utc', now())
  where public.profiles.id = new.id;

  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
after insert on auth.users
for each row
execute function public.handle_new_auth_user();

drop trigger if exists on_auth_user_updated on auth.users;
create trigger on_auth_user_updated
after update of email, raw_user_meta_data, email_confirmed_at, confirmed_at on auth.users
for each row
execute function public.sync_auth_user_profile();

create or replace function public.user_has_role(target_user_id uuid, target_role_code text)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.user_roles ur
    join public.app_roles ar
      on ar.code = ur.role_code
    where ur.user_id = target_user_id
      and ur.role_code = lower(target_role_code)
      and ar.is_active = true
  );
$$;

create or replace function public.user_is_admin(target_user_id uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.profiles p
    where p.id = target_user_id
      and p.is_super_admin = true
  ) or public.user_has_role(target_user_id, 'admin');
$$;

create or replace function public.user_can_access_module(target_user_id uuid, target_module_code text)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.user_roles ur
    join public.role_module_access rma
      on rma.role_code = ur.role_code
    join public.app_modules am
      on am.code = rma.module_code
    where ur.user_id = target_user_id
      and rma.module_code = lower(target_module_code)
      and rma.can_view = true
      and am.is_active = true
  ) or public.user_is_admin(target_user_id);
$$;

alter table public.profiles enable row level security;
alter table public.app_roles enable row level security;
alter table public.user_roles enable row level security;
alter table public.app_modules enable row level security;
alter table public.role_module_access enable row level security;

drop policy if exists "profiles_select_self_or_admin" on public.profiles;
create policy "profiles_select_self_or_admin"
on public.profiles
for select
to authenticated
using (
  auth.uid() = id
  or public.user_is_admin(auth.uid())
);

drop policy if exists "profiles_update_self_or_admin" on public.profiles;
create policy "profiles_update_self_or_admin"
on public.profiles
for update
to authenticated
using (
  auth.uid() = id
  or public.user_is_admin(auth.uid())
)
with check (
  auth.uid() = id
  or public.user_is_admin(auth.uid())
);

drop policy if exists "app_roles_select_authenticated" on public.app_roles;
create policy "app_roles_select_authenticated"
on public.app_roles
for select
to authenticated
using (is_active = true);

drop policy if exists "app_modules_select_authenticated" on public.app_modules;
create policy "app_modules_select_authenticated"
on public.app_modules
for select
to authenticated
using (is_active = true);

drop policy if exists "role_module_access_select_authenticated" on public.role_module_access;
create policy "role_module_access_select_authenticated"
on public.role_module_access
for select
to authenticated
using (true);

drop policy if exists "user_roles_select_self_or_admin" on public.user_roles;
create policy "user_roles_select_self_or_admin"
on public.user_roles
for select
to authenticated
using (
  auth.uid() = user_id
  or public.user_is_admin(auth.uid())
);

drop policy if exists "user_roles_manage_admin_only" on public.user_roles;
create policy "user_roles_manage_admin_only"
on public.user_roles
for all
to authenticated
using (public.user_is_admin(auth.uid()))
with check (public.user_is_admin(auth.uid()));

grant select, update on public.profiles to authenticated;
grant select on public.app_roles to authenticated;
grant select on public.app_modules to authenticated;
grant select on public.role_module_access to authenticated;
grant select, insert, update, delete on public.user_roles to authenticated;

