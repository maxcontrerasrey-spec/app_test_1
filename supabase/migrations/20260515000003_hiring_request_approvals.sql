after insert or update or delete on public.hiring_request_approvals
for each row execute function public.handle_hiring_request_approval_change();

create or replace function public.create_hiring_request(
  p_requested_entry_date date,
  p_job_position_id bigint,
  p_job_position_name text,
  p_vacancies integer,
  p_contract_id bigint,
  p_contract_name text,
  p_contract_number text,
  p_cost_unit text,
  p_cost_unit_name text,
  p_cost_center_code text,
  p_cost_center_name text,
  p_start_date date,
  p_end_date date,
  p_campamento boolean,
  p_pasajes boolean,
  p_other_benefits text,
  p_salary_offer numeric,
  p_shift_id bigint,
  p_shift_name text,
  p_requester_signed boolean default true
)
returns table (request_id uuid, folio text)
language plpgsql
security definer
set search_path = public
as $$
declare
  current_user_id uuid := auth.uid();
  requester_profile public.profiles%rowtype;
  next_folio text;
  created_request_id uuid;
  config_record record;
begin
  if current_user_id is null then
    raise exception 'Usuario no autenticado';
  end if;

  if not public.user_can_access_module(current_user_id, 'solicitud_contrataciones') then
    raise exception 'El usuario no tiene acceso para crear solicitudes de contratación';
  end if;

  select *
    into requester_profile
    from public.profiles
   where id = current_user_id;

  if requester_profile.id is null then
    raise exception 'No existe perfil para el usuario autenticado';
  end if;

  next_folio := lpad(nextval('public.hiring_folio_seq')::text, 4, '0');

  insert into public.hiring_requests (
    folio,
    requester_id,
    requester_name,
    requester_job_title,
    requester_email,
    requested_entry_date,
    job_position_id,
    job_position_name,
    vacancies,
    contract_id,
    contract_name,
    contract_number,
    cost_unit,
    cost_unit_name,
    cost_center_code,
    cost_center_name,
    start_date,
    end_date,
    campamento,
    pasajes,
    other_benefits,
    salary_offer,
    shift_id,
    shift_name,
    requester_signed,
    status
  )
  values (
    next_folio,
    current_user_id,
    coalesce(requester_profile.full_name, requester_profile.email),
    requester_profile.job_title,
    requester_profile.email,
    p_requested_entry_date,
    p_job_position_id,
    p_job_position_name,
    p_vacancies,
    p_contract_id,
    p_contract_name,
    p_contract_number,
    p_cost_unit,
    p_cost_unit_name,
    p_cost_center_code,
    p_cost_center_name,
    p_start_date,
    p_end_date,
    p_campamento,
    p_pasajes,
    p_other_benefits,
    p_salary_offer,
    p_shift_id,
    p_shift_name,
    p_requester_signed,
    'pendiente'
  )
  returning id into created_request_id;

  insert into public.hiring_request_approvals (
    hiring_request_id,
    step_code,
    step_name,
    step_order,
    approver_user_id,
    approver_name,
    approver_email,
    status,
    decided_at
  )
  values (
    created_request_id,
    'requester_signature',
    'Firma solicitante',
    1,
    current_user_id,
    coalesce(requester_profile.full_name, requester_profile.email),
    requester_profile.email,
    case when p_requester_signed then 'approved' else 'pending' end,
    case when p_requester_signed then timezone('utc', now()) else null end
  );

  for config_record in
    select
      hac.step_code,
      hac.step_name,
      hac.step_order,
      p.id as approver_user_id,
      p.full_name,
      p.email
    from public.hiring_approval_configs hac
    join public.profiles p
      on p.id = hac.approver_user_id
    where hac.is_active = true
    order by hac.step_order
  loop
    insert into public.hiring_request_approvals (
      hiring_request_id,
      step_code,
      step_name,
      step_order,
      approver_user_id,
      approver_name,
      approver_email,
      status
    )
    values (
      created_request_id,
      config_record.step_code,
      config_record.step_name,
      config_record.step_order,
      config_record.approver_user_id,
      coalesce(config_record.full_name, config_record.email),
      config_record.email,
      'pending'
    )
    on conflict (hiring_request_id, step_code) do nothing;
  end loop;

  perform public.refresh_hiring_request_status(created_request_id);

  return query
  select created_request_id, next_folio;
end;
$$;

create or replace function public.decide_hiring_request_approval(
  p_approval_id bigint,
  p_decision text,
  p_comments text default null
)
returns uuid
language plpgsql
security definer
set search_path = public
as $$
declare
  current_user_id uuid := auth.uid();
  approval_record public.hiring_request_approvals%rowtype;
begin
  if current_user_id is null then
    raise exception 'Usuario no autenticado';
  end if;

  if p_decision not in ('approved', 'rejected') then
    raise exception 'Decisión inválida';
  end if;

  select *
    into approval_record
    from public.hiring_request_approvals
   where id = p_approval_id;

  if approval_record.id is null then
    raise exception 'No existe la aprobación solicitada';
  end if;

  if approval_record.step_code = 'requester_signature' then
    raise exception 'La firma del solicitante no se puede decidir por esta vía';
  end if;

  if approval_record.approver_user_id <> current_user_id and not public.user_is_admin(current_user_id) then
    raise exception 'El usuario no está autorizado para decidir esta aprobación';
  end if;

  update public.hiring_request_approvals
     set status = p_decision,
         comments = p_comments,
         decided_at = timezone('utc', now()),
         updated_at = timezone('utc', now())
   where id = p_approval_id;

  perform public.refresh_hiring_request_status(approval_record.hiring_request_id);

  return approval_record.hiring_request_id;
end;
$$;

