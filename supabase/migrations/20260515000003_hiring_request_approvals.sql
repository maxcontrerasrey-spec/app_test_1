create or replace function public.refresh_hiring_request_status(target_request_id uuid)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  has_rejection boolean;
  has_operational_step boolean;
  has_contracts_step boolean;
  has_pending boolean;
  all_required_approved boolean;
begin
  select exists (
    select 1
    from public.hiring_request_approvals
    where hiring_request_id = target_request_id
      and status = 'rejected'
  ) into has_rejection;

  if has_rejection then
    update public.hiring_requests
    set status = 'rechazada',
        updated_at = timezone('utc', now())
    where id = target_request_id;
    return;
  end if;

  select exists (
    select 1
    from public.hiring_request_approvals
    where hiring_request_id = target_request_id
      and step_code = 'operational_approval'
  ) into has_operational_step;

  select exists (
    select 1
    from public.hiring_request_approvals
    where hiring_request_id = target_request_id
      and step_code = 'contracts_control'
  ) into has_contracts_step;

  select exists (
    select 1
    from public.hiring_request_approvals
    where hiring_request_id = target_request_id
      and step_code in ('operational_approval', 'contracts_control')
      and status = 'pending'
  ) into has_pending;

  select coalesce(bool_and(status = 'approved'), false)
    from public.hiring_request_approvals
   where hiring_request_id = target_request_id
     and step_code in ('requester_signature', 'operational_approval', 'contracts_control')
  into all_required_approved;

  if not has_operational_step or not has_contracts_step or has_pending or not all_required_approved then
    update public.hiring_requests
    set status = 'pendiente',
        updated_at = timezone('utc', now())
    where id = target_request_id;
    return;
  end if;

  update public.hiring_requests
  set status = 'aprobada',
      updated_at = timezone('utc', now())
  where id = target_request_id;
end;
$$;

create or replace function public.handle_hiring_request_approval_change()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  perform public.refresh_hiring_request_status(coalesce(new.hiring_request_id, old.hiring_request_id));
  return coalesce(new, old);
end;
$$;

