insert into public.equipment (
  equipment_code,
  plate,
  equipment_type,
  current_client,
  brand,
  model,
  year,
  is_active
)
select
  equipment_code,
  plate,
  equipment_type,
  current_client,
  brand,
  model,
  year,
  true
from operations_fleet_seed
on conflict (equipment_code) do update
set
  plate = excluded.plate,
  equipment_type = excluded.equipment_type,
  current_client = excluded.current_client,
  brand = excluded.brand,
  model = excluded.model,
  year = excluded.year,
  is_active = true,
  updated_at = timezone('utc', now());

update public.equipment e
   set is_active = false,
       updated_at = timezone('utc', now())
 where not exists (
   select 1
     from operations_fleet_seed seed
    where seed.equipment_code = e.equipment_code
 );

